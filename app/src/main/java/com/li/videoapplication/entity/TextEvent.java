public class TextEvent {
    private int msg;

    public TextEvent() {
    }

    public TextEvent(int msg) {
        this.msg = msg;
    }

    public int getMsg() {
        return msg;
    }

    public void setMsg(int msg) {
        this.msg = msg;
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
